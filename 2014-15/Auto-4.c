task initialize_motors()
{
	bool first_time_repeat = true;
	bool check_spatula = false;
	bool lockout_medium = true;
	wait1Msec(800);
	motor[FORK] = -100;
	//start timer
	ClearTimer(T2);
	time1[T2] = 0;
	motor[TUBE] = 100;
	while (!check_spatula) {
		while (!(SPATULA_DOWN & 0x08)) {/*when timer is greater than or equal to the time to get down the ramp clear the timer and start lifting the tube*/
		}
		if (SPATULA_DOWN & 0x08) {
			check_spatula = true;
			} else {
			check_spatula = false;
		}
	}
	//when timer is greater than or equal to 7000 miliseconds stop the lift
	motor[FORK] = 0;

	while (time1[T2] < 9000 && !SIXTY_REACHED) {}
	motor[TUBE] = 0;
	ClearTimer(T2);
	lockout_medium = false;
}

task tele_setup()
{
	GRAB_OPEN;
	bool setup_done = false;
	bool fork_down = true;
	if (!SPATULA_DOWN) {
		motor[FORK] = -100;
		fork_down = false;
	}
	if (!MAX_REACHED) {
		motor[TUBE] = 100;
	}
	while (!MAX_REACHED) {
		if (SPATULA_DOWN && !fork_down) {
			if (SPATULA_DOWN) {
				motor[FORK] = 0;
				fork_down = true;
			}
		}
	}
	motor[TUBE] = 0;
	while (!fork_down) {
		if (SPATULA_DOWN) {
			motor[FORK] = 0;
			fork_down = true;
		}
	}
	setup_done = true;
	halt();
	wait1Msec(500);
}

