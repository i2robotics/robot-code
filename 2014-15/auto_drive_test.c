void pop_it(int times, bool feeder)
{
#ifndef DEBUG_NO_POP
	bool is_cocked = false;
	if (feeder)
		motor[FEEDER] = 100;

	motor[POPPER] = 100;
	while (times >= 0) {
		if (POPPER_PRIMED && !is_cocked) {
			is_cocked = true;
			times--;
			motor[POPPER] = 0;
			wait1Msec(50);
			motor[POPPER] = 100;
		} else if (!POPPER_PRIMED) {
			is_cocked = false;
		}
	}
#else
	PlayImmediateTone(900,20);
	wait1Msec(1000);
#endif
	motor[POPPER] = 0;
	motor[FEEDER] = 0;
}

